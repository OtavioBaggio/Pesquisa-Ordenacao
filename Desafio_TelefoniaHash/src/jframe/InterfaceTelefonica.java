    private void btn_buscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_buscarActionPerformed
        String numeroCelular = txt_numero.getText().trim();
        
        //requestFocus é muto brabo, ele já coloca o cursor ja no campo, sem o usuário precisar clicar
        if(numeroCelular.isEmpty()){
            JOptionPane.showMessageDialog(this, "Por favor digite um número de celular!", "campo vazio", JOptionPane.WARNING_MESSAGE);
            txt_numero.requestFocus();
            return;
        }
        
        if(sessoes.isEmpty()){
            JOptionPane.showMessageDialog(this, "Nenhuma sessão carregada!\nPor favor, carregue um arquivo primeiro..", "Lista vazia",
                    JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        Sessao sessaoEncontrada = sessoes.get(numeroCelular);
        
        if (sessaoEncontrada != null) {
            //Formata os dados para exibição
            String resultado = "[OK] SESSÃO ENCONTRADA\n";
            resultado += "=========================================\n\n";
            resultado += "Número de Celular: " + sessaoEncontrada.numeroCelular + "\n\n";
            resultado += "Dados da Sessão:\n";
            resultado += formatarJSON(sessaoEncontrada.dadosSessao);
            
            txtA_resultados.setText(resultado);
        } else {
            String resultado = "[FALHA] SESSÃO NÃO ENCONTRADA\n\n";
            resultado += "O número " + numeroCelular + " não foi encontrado na base de dados.\n";
            resultado += "Verifique se o número está correto e tente novamente.";
            
            txtA_resultados.setText(resultado);
        }
    }//GEN-LAST:event_btn_buscarActionPerformed

    private String formatarJSON(String json){
        // Remove chaves do início e fim
        json = json.trim();
        if (json.startsWith("{")) {
            json = json.substring(1);
        }
        if (json.endsWith("}")) {
            json = json.substring(0, json.length() - 1);
        }
    
        // Separa por vírgulas e formata cada campo
        String[] campos = json.split(",(?=\\\"[^\\\"]*\\\":)");
        StringBuilder resultado = new StringBuilder();
        
        for (String campo : campos) {
            campo = campo.trim();
            // Separa chave e valor
            if (campo.contains(":")) {
                String[] partes = campo.split(":", 2);
                String chave = partes[0].replace("\"", "").trim();
                String valor = partes[1].trim();
                
                // Formata a linha
                resultado.append(String.format("%-15s : %s\n", chave, valor));
            }
        }
        
        return resultado.toString();
    }
    
