    private void btn_carregarArquivoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_carregarArquivoActionPerformed
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setDialogTitle("Selecione o arquivo de sessões");
        
        // Filtro para arquivos .txt
        FileNameExtensionFilter filter = new FileNameExtensionFilter(
            "Arquivos de texto (*.txt)", "txt"
        );
        fileChooser.setFileFilter(filter);
        
        int resultado = fileChooser.showOpenDialog(this);
        
        if (resultado == JFileChooser.APPROVE_OPTION) {
            String caminho = fileChooser.getSelectedFile().getAbsolutePath();
            
            try {
                // Limpa sessões anteriores
                sessoes.clear();
                
                // Carrega o arquivo usando a classe Arquivo
                Arquivo.carregarArquivo(sessoes, caminho);
                
                // Atualiza o contador de registros
                lbl_numerosCarregados.setText(String.valueOf(sessoes.size()));
                
                // Mensagem de sucesso
                JOptionPane.showMessageDialog(
                    this, "Arquivo carregado com sucesso!\n" + sessoes.size() + " registros foram importados.",
                    "Sucesso", JOptionPane.INFORMATION_MESSAGE
                );
                
                // Limpa os campos
                txt_numero.setText("");
                txtA_resultados.setText("");
                txt_numero.requestFocus();
                
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(
                    this,
                    "Erro ao carregar o arquivo:\n" + ex.getMessage(),
                    "Erro",
                    JOptionPane.ERROR_MESSAGE
                );
            }
        }
    }//GEN-LAST:event_btn_carregarArquivoActionPerformed

